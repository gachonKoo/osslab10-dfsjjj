import sys

# 명령줄 인수로 입력된 숫자를 정수로 변환합니다.
# 주의: 슬라이드에 명시된 대로 sys.argv[1]을 사용합니다! 
# (이전 <stdin>은 무시하고 슬라이드를 따릅니다.)
number = int(sys.argv[1])

# 약수 목록을 위한 반복문
for i in range(1, number + 1):  # <<------ [빈칸 1]
    
    # number를 i로 나누어 나머지가 0인지 검사 (약수인지 확인)
    if number % i == 0:         # <<------ [빈칸 2]
        
        # 약수를 출력하되, 줄바꿈 대신 공백(end=" ")을 사용
        print(i, end=" ")
        
# 모든 출력이 끝난 후, 줄바꿈(새 줄)을 출력하여 출력 형식을 완성
print()
